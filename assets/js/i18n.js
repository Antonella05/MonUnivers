function updateLanguage(lang) {
    // 1. Text Content
    document.querySelectorAll('[data-i18n]').forEach(element => {
        const key = element.getAttribute('data-i18n');
        if (translations[lang] && translations[lang][key]) {
            // Check if existing content has HTML tags (like <span> or <br>)
            // If the key specifically targets rich text (checked by key naming convention or content), allow HTML
            if (key.includes('title') || key.includes('text') || key === 'footer_copyright') {
                element.innerHTML = translations[lang][key];
            } else {
                element.innerText = translations[lang][key];
            }
        }
    });

    // 2. Placeholders for Inputs
    document.querySelectorAll('[data-i18n-placeholder]').forEach(element => {
        const key = element.getAttribute('data-i18n-placeholder');
        if (translations[lang] && translations[lang][key]) {
            element.setAttribute('placeholder', translations[lang][key]);
        }
    });

    // Update active class on switcher
    const switchers = document.querySelectorAll('.lang-switch-wrapper span');
    switchers.forEach(span => {
        if (span.getAttribute('data-lang') === lang) {
            span.classList.add('active');
        } else {
            span.classList.remove('active');
        }
    });

    localStorage.setItem('lang', lang);
}

document.addEventListener('DOMContentLoaded', () => {
    const savedLang = localStorage.getItem('lang') || 'fr';
    updateLanguage(savedLang);

    document.querySelectorAll('.lang-switcher').forEach(btn => {
        btn.addEventListener('click', (e) => {
            const lang = e.target.getAttribute('data-lang');
            if (lang) updateLanguage(lang);
        });
    });
});
